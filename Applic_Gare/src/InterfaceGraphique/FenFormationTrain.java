    private void btValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btValiderActionPerformed
        if (!tfLieuArrivee.getText().equals("") && !tfLieuDepart.getText().equals("") && tableTrains.getSelectedRow() >= 0)
        {
            System.out.println("Ajout : done");

            int rowIndex = tableTrains.getSelectedRow();
            Train TrainUtilise = vecTrainDepot.get(rowIndex);

            String HeureArrivee = cbArriveeHeure.getSelectedItem().toString() + ":" + cbArriveeMinutes.getSelectedItem().toString();
            String HeureDepart = cbDepartHeure.getSelectedItem().toString() + ":" + cbDepartMinutes.getSelectedItem().toString();
            NewHoraireTrain = new HoraireTrain(TrainUtilise, HeureArrivee, HeureDepart, cbType.getSelectedItem().toString(), tfLieuDepart.getText(), tfLieuArrivee.getText(), Integer.parseInt(cbQuai.getSelectedItem().toString()));
            
            setValide(true);
            
            this.dispose();
        }
        else
            System.out.println(" ! Champs incomplet ! ");
    }//GEN-LAST:event_btValiderActionPerformed

