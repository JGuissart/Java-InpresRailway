    public void LoadHoraireTrain()
    {
        File f = new File(cheminFichier + "Applic_Gare" + sep + "HoraireTrainLiege.dat");
        
        try
        {
            if (f.exists()) // Le fichier existe -> On charge les données
            {
                ObjectLoader OL = new ObjectLoader(cheminFichier + "Applic_Gare" + sep + "HoraireTrainLiege.dat");
                vctHoraireTrain = (Vector<HoraireTrain>)OL.load();
            }
            else
                System.out.println("Fichier inexistant");
        }
        catch (IOException ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            Logger.getLogger(FenApplicDepot.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (ClassNotFoundException ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            Logger.getLogger(FenApplicDepot.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void LoadPropertiesFile()
    {
        PropertyFile = new Properties();
        try
        {
            FileInputStream FIS = new FileInputStream(cheminFichier + "Applic_Depot" + sep + "ressources" + sep + "ApplicDepotProperties.properties");
            try
            {
                PropertyFile.load(FIS);
            }
            catch (IOException ex)
            {
                Logger.getLogger(FenApplicDepot.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
        catch (FileNotFoundException ex)
        {
            Logger.getLogger(FenApplicDepot.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

