    public FenFormationTrain(java.awt.Frame parent, boolean modal, Vector<Train> vctTrain) 
    {
        super(parent, modal);        
        initComponents();
        DefaultTableModel md = (DefaultTableModel)tableTrains.getModel();
        
        setTitle("Formation de trains");
        
        vecTrainDepot = vctTrain;
        
        for (int i = 0; i < vecTrainDepot.size(); i++)
        {
            md.addRow(new Object[]
            {vecTrainDepot.get(i).toString()});
        }
        
        tfLieuArrivee.setText(null);
        tfLieuDepart.setText(null);
        
        cbArriveeHeure.removeAllItems();
        cbArriveeMinutes.removeAllItems();
        cbDepartHeure.removeAllItems();
        cbDepartMinutes.removeAllItems();
        
        for (int i = 0; i < 60; i++)
        {
            if (i < 10)
            {
                cbArriveeHeure.addItem("0" + i);
                cbArriveeMinutes.addItem("0" + i);
                cbDepartHeure.addItem("0" + i);
                cbDepartMinutes.addItem("0" + i);
            }
            else
            {
                if (i < 24)
                {
                    cbArriveeHeure.addItem(i);
                    cbDepartHeure.addItem(i);
                }
                
                cbArriveeMinutes.addItem(i);
                cbDepartMinutes.addItem(i);
            }
        }
    }

