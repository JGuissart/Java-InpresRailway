            NSS = new NetworkStringSender((String)PropertyFile.get("AdresseReseau"), Integer.valueOf((String)PropertyFile.get("PortNSS")));
        
        NSS.sendString("ACK");
        tfAnnonce.setText(null);
        btMessageRecu.setEnabled(false);
