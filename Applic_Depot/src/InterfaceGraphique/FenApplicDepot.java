    private void btValiderVoieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btValiderVoieActionPerformed
        for (int i = 0; i < vctHoraireTrain.size(); i++)
        {
            System.out.println(vctHoraireTrain.get(i).getTrain().getIdentifiant()+ " - " + tfTrainConsidere.getText().substring(0, 7));
            
            if (vctHoraireTrain.get(i).getTrain().getIdentifiant().equals(tfTrainConsidere.getText().substring(0, 7)))
            {
                DefaultTableModel md = (DefaultTableModel)tableOccupationVoie.getModel();
                md.setValueAt(vctHoraireTrain.get(i).getTrain().getIdentifiant(), cbVoieChoisie.getSelectedIndex(), 1);
                md.setValueAt(vctHoraireTrain.get(i).getHeureArrivee(), cbVoieChoisie.getSelectedIndex(), 2);
                md.setValueAt(vctHoraireTrain.get(i).getTrain().getVecWagon().size(), cbVoieChoisie.getSelectedIndex(), 3);
                
                break;
            }
        }
        cbVoieChoisie.removeItemAt(cbVoieChoisie.getSelectedIndex());
        tfTrainConsidere.setText(null);
        btMessageRecu.setEnabled(true);
    }//GEN-LAST:event_btValiderVoieActionPerformed

