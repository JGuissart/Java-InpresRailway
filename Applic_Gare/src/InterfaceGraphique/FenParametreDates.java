    private void btValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btValiderActionPerformed

        Locale Pays = Locale.FRANCE;
        int DF = 0;
        int DFDate = 0;

        switch(cbPays.getSelectedIndex())
        {
            case 0:
                Pays = Locale.FRANCE;
                break;
            case 1:
                Pays = Locale.UK;
                break;
            case 2:
                Pays = Locale.GERMANY;
                break;
            case 3:
                Pays = Locale.ITALY;
                break;
            case 4:
                Pays = Locale.US;
                break;
        }

        switch (cbFormatDate.getSelectedIndex())
        {
            case 0:
                DFDate = DateFormat.SHORT;
                break;
            case 1:
                DFDate = DateFormat.MEDIUM;
                break;
            case 2:
                DFDate = DateFormat.FULL;
                break;
        }

        switch (cbFormatHeure.getSelectedIndex())
        {
            case 0:
                DF = DateFormat.SHORT;
                break;
            case 1:
                DF = DateFormat.MEDIUM;
                break;
            case 2:
                DF = DateFormat.FULL;
                break;
        }
        Date Now = new Date();
        setDate(DateFormat.getDateTimeInstance(DFDate,DF, Pays).format(Now));
        this.dispose();
    }//GEN-LAST:event_btValiderActionPerformed

